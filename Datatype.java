class Datatype{
    public static void main(String args[]){
    int a;
    long days;
    short hr;
    char ch1;
    float r;
    double d;
    boolean b;

    a=10;
    days=1500;
    hr=2;
    ch1='y';
    r=10.5f;
    d=3.1467;
    b=false;
    System.out.println("a="+a);	//a=10
    System.out.println("days="+days);	//days=1500
    System.out.println("hr="+hr);	//hr=2
    System.out.println("ch1="+ch1);		//ch1=y
    System.out.println("r="+r);		//r=10.0
    System.out.println("d="+d);	//d=3.1467
    System.out.println("b="+b);	//b=false
}
}
